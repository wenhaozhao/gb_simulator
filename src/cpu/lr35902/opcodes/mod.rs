/// ## get_opcode
/// ### addr
/// - instruction set 0x00_XX
/// - ext instruction (prefix cb) 0xCB_XX
pub fn get_opcode(addr: u16) -> Option<&'static dyn Opcode> {
    let opcode_index = if addr & 0xCB00 == 0xCB00 {
        (addr & 0x00FF) | 0x0100
    } else {
        addr
    } as usize;
    OPCODES[opcode_index]
}

